void main() {
  // Declare variables of various types
  int integerVar = 10;
  double doubleVar = 20.5;
  String stringVar = "Hello, Dart!";
  bool boolVar = true;

  // Perform basic operations
  int sum = integerVar + 5; // Addition
  double product = doubleVar * 2; // Multiplication
  String greeting = stringVar + " How are you?"; // Concatenation
  bool negation = !boolVar; // Negation

  // Print the results
  print("Integer Variable: $integerVar");
  print("Double Variable: $doubleVar");
  print("String Variable: $stringVar");
  print("Boolean Variable: $boolVar");
  print("Sum: $sum");
  print("Product: $product");
  print("Greeting: $greeting");
  print("Negation: $negation");
}
